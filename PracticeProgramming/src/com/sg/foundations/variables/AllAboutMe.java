package com.sg.foundations.variables;

public class AllAboutMe {
	public static void main(String[] args) {
		String name, fav_food, live;
		int pets;
		boolean canWhistle;
		
		name = "Ada Lovelace";
		fav_food = "hot buttered gnocchi";
		pets = 5;
		live = "pineapple under the sea";
		canWhistle = true;
		
		System.out.println("My name is " + name + ".");
		System.out.println("My favorite food is " + fav_food + ".");
		System.out.println("I have " + pets + ".");
		System.out.println("I live in a " + live + ", and I love it here.");
		System.out.println("It is " + canWhistle + " I know how to whistle.");
	}
}
